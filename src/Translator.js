  turkishToEnglish(text){
    var trMap = {
      'çÇ':'c',
      'ğĞ':'g',
      'şŞ':'s',
      'üÜ':'u',
      'ıİ':'i',
      'öÖ':'o'
    };
    for(var key in trMap) {
        text = text.replace(new RegExp('['+key+']','g'), trMap[key]);
    }
    return text;
  }


  swapAlphabet(alphabet){
    var ret = {};
    for(var key in alphabet){
      ret[alphabet[key]] = key;
    }
    return ret;
  }

